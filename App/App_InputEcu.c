void app_input_ecu_init(void)
{
    drv_button_init();
    drv_adc_input_init();
    if_input_ecu_init();
    can_tx_input_init();
}

void app_input_ecu_task_10ms(void)
{
    drv_button_task();
    drv_adc_input_task();
    if_input_ecu_task();
}

void app_input_ecu_task_10ms_can_tx(void)
{
    const if_input_ecu_data_t* input_data;

    input_data = if_input_ecu_get_data();
    can_tx_input_send(input_data);
}
